-- RECONCILIATION_STATUS: drives row-level badge color
--   CONFIRMED = LOADED or FAILED-with-error (green)
--   UNRECONCILED = FAILED with no error text (red)
--   IN_PROGRESS = GENERATED or earlier (yellow)
