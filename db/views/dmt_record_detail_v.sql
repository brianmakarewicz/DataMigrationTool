       q.TFM_SEQUENCE_ID, q.STG_SEQUENCE_ID, q.RUN_ID, q.RUN_ID AS INTEGRATION_ID,
       q.DISPLAY_KEY, q.LOOKUP_KEY, q.TFM_STATUS, q.ERROR_TEXT,
