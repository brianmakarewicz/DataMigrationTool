           WHEN TFM_STATUS = 'LOADED' THEN 'CONFIRMED'
           WHEN TFM_STATUS = 'FAILED' AND ERROR_TEXT IS NOT NULL THEN 'CONFIRMED'
           WHEN TFM_STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 'UNRECONCILED'
