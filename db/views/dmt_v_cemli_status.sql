-- DMT_V_CEMLI_STATUS
CREATE OR REPLACE FORCE EDITIONABLE VIEW "DMT_V_CEMLI_STATUS" ("PIPELINE", "CEMLI_CODE", "SORT_ORDER", "RUN_ID", "INTEGRATION_ID", "STATUS", "ROW_COUNT", "UNRECONCILED_COUNT")  AS 
  SELECT q.PIPELINE, q.CEMLI_CODE, q.SORT_ORDER,
       q.RUN_ID, q.RUN_ID AS INTEGRATION_ID,
       q.STATUS, q.ROW_COUNT, q.UNRECONCILED_COUNT
FROM (
-- P2P Pipeline
SELECT 'P2P' AS PIPELINE, 'Suppliers' AS CEMLI_CODE, 1 AS SORT_ORDER,
       RUN_ID, STATUS, COUNT(*) AS ROW_COUNT,
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END) AS UNRECONCILED_COUNT
FROM DMT_OWNER.DMT_POZ_SUPPLIERS_TFM_TBL GROUP BY RUN_ID, STATUS
UNION ALL
SELECT 'P2P', 'SupplierAddresses', 2, RUN_ID, STATUS, COUNT(*),
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_POZ_SUP_ADDR_TFM_TBL GROUP BY RUN_ID, STATUS
UNION ALL
SELECT 'P2P', 'SupplierSites', 3, RUN_ID, STATUS, COUNT(*),
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_POZ_SUP_SITE_TFM_TBL GROUP BY RUN_ID, STATUS
UNION ALL
SELECT 'P2P', 'SupplierSiteAssignments', 4, RUN_ID, STATUS, COUNT(*),
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_POZ_SUP_SITE_ASSN_TFM_TBL GROUP BY RUN_ID, STATUS
UNION ALL
SELECT 'P2P', 'SupplierContacts', 5, RUN_ID, STATUS, COUNT(*),
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_POZ_SUP_CONTACTS_TFM_TBL GROUP BY RUN_ID, STATUS
UNION ALL
SELECT 'P2P', 'PurchaseOrders', 6, RUN_ID, STATUS, COUNT(*),
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_PO_HEADERS_INT_TFM_TBL
WHERE STYLE_DISPLAY_NAME = 'Purchase Order' OR STYLE_DISPLAY_NAME IS NULL
GROUP BY RUN_ID, STATUS
UNION ALL
SELECT 'P2P', 'BlanketPOs', 7, RUN_ID, STATUS, COUNT(*),
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_PO_HEADERS_INT_TFM_TBL
WHERE STYLE_DISPLAY_NAME = 'Blanket Purchase Agreement'
GROUP BY RUN_ID, STATUS
UNION ALL
SELECT 'P2P', 'Contracts', 8, RUN_ID, STATUS, COUNT(*),
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_PO_HEADERS_INT_TFM_TBL
WHERE STYLE_DISPLAY_NAME = 'Contract Purchase Agreement'
GROUP BY RUN_ID, STATUS
UNION ALL
SELECT 'P2P', 'APInvoices', 9, RUN_ID, STATUS, COUNT(*),
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_AP_INVOICES_INT_TFM_TBL
WHERE INVOICE_TYPE_LOOKUP_CODE NOT LIKE '%1099%' OR INVOICE_TYPE_LOOKUP_CODE IS NULL
GROUP BY RUN_ID, STATUS
UNION ALL
SELECT 'P2P', '1099Invoices', 10, RUN_ID, STATUS, COUNT(*),
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_AP_INVOICES_INT_TFM_TBL
WHERE INVOICE_TYPE_LOOKUP_CODE LIKE '%1099%'
GROUP BY RUN_ID, STATUS
-- O2C Pipeline
UNION ALL
SELECT 'O2C', 'Customers', 1, RUN_ID, STATUS, COUNT(*),
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_HZ_PARTIES_TFM_TBL GROUP BY RUN_ID, STATUS
UNION ALL
SELECT 'O2C', 'ARInvoices', 2, RUN_ID, STATUS, COUNT(*),
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_RA_LINES_TFM_TBL GROUP BY RUN_ID, STATUS
-- Projects Pipeline
UNION ALL
SELECT 'Projects', 'Projects', 1, RUN_ID, STATUS, COUNT(*),
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_PJF_PROJECTS_TFM_TBL GROUP BY RUN_ID, STATUS
UNION ALL
SELECT 'Projects', 'BillingEvents', 2, RUN_ID, STATUS, COUNT(*),
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_PJB_BILL_EVENTS_TFM_TBL GROUP BY RUN_ID, STATUS
UNION ALL
SELECT 'Projects', 'Expenditures', 3, RUN_ID, STATUS, COUNT(*),
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_PJC_EXPENDITURES_TFM_TBL GROUP BY RUN_ID, STATUS
UNION ALL
SELECT 'Projects', 'Grants', 4, RUN_ID, STATUS, COUNT(*),
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_GMS_AWD_HEADERS_TFM_TBL GROUP BY RUN_ID, STATUS
UNION ALL
SELECT 'Projects', 'ProjectBudgets', 5, RUN_ID, STATUS, COUNT(*),
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_PRJ_BUDGET_TFM_TBL GROUP BY RUN_ID, STATUS
-- Financials Pipeline
UNION ALL
SELECT 'Financials', 'GLBalances', 1, RUN_ID, TFM_STATUS AS STATUS, COUNT(*),
       SUM(CASE WHEN TFM_STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_GL_INTERFACE_TFM_TBL GROUP BY RUN_ID, TFM_STATUS
UNION ALL
SELECT 'Financials', 'GLBudgetBalances', 2, RUN_ID, TFM_STATUS AS STATUS, COUNT(*),
       SUM(CASE WHEN TFM_STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_GL_BUDGET_INT_TFM_TBL GROUP BY RUN_ID, TFM_STATUS
UNION ALL
SELECT 'Financials', 'PlanningBudgets', 3, RUN_ID, STATUS, COUNT(*),
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_PLAN_BUDGET_TFM_TBL GROUP BY RUN_ID, STATUS
UNION ALL
SELECT 'Financials', 'Assets', 4, RUN_ID, STATUS, COUNT(*),
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_FA_ASSET_HDR_TFM_TBL GROUP BY RUN_ID, STATUS
-- Standalone
UNION ALL
SELECT 'Standalone', 'Requisitions', 1, RUN_ID, STATUS, COUNT(*),
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_POR_REQ_HEADERS_TFM_TBL GROUP BY RUN_ID, STATUS
UNION ALL
SELECT 'Standalone', 'MiscReceipts', 2, RUN_ID, STATUS, COUNT(*),
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_RCV_HEADERS_TFM_TBL GROUP BY RUN_ID, STATUS
-- HCM Pipeline
UNION ALL
SELECT 'HCM', 'Workers', 1, RUN_ID, STATUS, COUNT(*),
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
FROM DMT_OWNER.DMT_WORKER_TFM_TBL GROUP BY RUN_ID, STATUS
UNION ALL
SELECT 'HCM', 'Assignments', 2, RUN_ID, STATUS, COUNT(*),
       SUM(CASE WHEN STATUS = 'FAILED' AND ERROR_TEXT IS NULL THEN 1 ELSE 0 END)
