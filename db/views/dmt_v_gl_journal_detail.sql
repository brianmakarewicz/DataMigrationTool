  SELECT
    -- 1. Run status
    NVL(tfm.TFM_STATUS, stg.STG_STATUS)                        AS RUN_STATUS,
    -- 2. Error text
    tfm.ERROR_TEXT                                           AS ERROR_TEXT,
    CASE
        WHEN NVL(tfm.TFM_STATUS, stg.STG_STATUS) = 'LOADED' THEN 'CONFIRMED'
        WHEN NVL(tfm.TFM_STATUS, stg.STG_STATUS) = 'FAILED' AND tfm.ERROR_TEXT IS NOT NULL THEN 'CONFIRMED'
        WHEN NVL(tfm.TFM_STATUS, stg.STG_STATUS) = 'FAILED' AND tfm.ERROR_TEXT IS NULL THEN 'UNRECONCILED'
        ELSE 'IN_PROGRESS'
    END                                                      AS RECONCILIATION_STATUS,
    -- 3. Human identifiers
    stg.LEDGER_NAME                                         AS LEDGER_NAME,
    stg.USER_JE_CATEGORY_NAME                               AS USER_JE_CATEGORY_NAME,
    -- 4. All remaining STG business columns
    stg.JOURNAL_STATUS,
    stg.ACCOUNTING_DATE,
    stg.CURRENCY_CODE,
    stg.DATE_CREATED,
    stg.CREATED_BY,
    stg.ACTUAL_FLAG,
    stg.USER_JE_SOURCE_NAME,
    stg.CURRENCY_CONVERSION_DATE,
    stg.USER_CURRENCY_CONVERSION_TYPE,
    stg.CURRENCY_CONVERSION_RATE,
    stg.SEGMENT1,
    stg.SEGMENT2,
    stg.SEGMENT3,
    stg.SEGMENT4,
    stg.SEGMENT5,
