    stg.SEGMENT20,
    stg.SEGMENT21,
    stg.SEGMENT22,
    stg.SEGMENT23,
    stg.SEGMENT24,
    stg.SEGMENT25,
    stg.SEGMENT26,
    stg.SEGMENT27,
    stg.SEGMENT28,
    stg.SEGMENT29,
    stg.SEGMENT30,
    stg.ENTERED_DR,
    stg.ENTERED_CR,
    stg.ACCOUNTED_DR,
    stg.ACCOUNTED_CR,
    stg.REFERENCE1,
    stg.REFERENCE2,
    stg.REFERENCE4,
    stg.REFERENCE5,
    stg.REFERENCE6,
    stg.REFERENCE7,
    stg.REFERENCE8,
    stg.REFERENCE10,
    stg.STAT_AMOUNT,
    stg.GROUP_ID,
    stg.PERIOD_NAME,
    stg.ATTRIBUTE_CATEGORY,
    stg.ATTRIBUTE1,
    stg.ATTRIBUTE2,
    stg.ATTRIBUTE3,
    stg.ATTRIBUTE4,
    stg.ATTRIBUTE5,
    stg.ATTRIBUTE6,
    stg.ATTRIBUTE7,
    stg.ATTRIBUTE8,
    stg.ATTRIBUTE9,
    stg.ATTRIBUTE10,
    stg.ATTRIBUTE11,
    stg.ATTRIBUTE12,
    stg.ATTRIBUTE13,
    stg.ATTRIBUTE14,
    stg.ATTRIBUTE15,
    stg.ATTRIBUTE16,
    stg.ATTRIBUTE17,
    stg.ATTRIBUTE18,
    stg.ATTRIBUTE19,
    stg.ATTRIBUTE20,
    stg.SOURCE_ID,
    -- 5. STG_SEQUENCE_ID
    stg.STG_SEQUENCE_ID,
    -- 6. RUN_ID
    tfm.RUN_ID,
    tfm.RUN_ID                                              AS INTEGRATION_ID,
    -- 7. PREFIX (from PIPELINE_RUN)
    cm.PREFIX,
    -- 8. SCENARIO_ID
    stg.SCENARIO_ID,
    -- 9. STAGE_DATE
    stg.STAGE_DATE,
    -- 10. LAST_UPDATED_DATE
    NVL(tfm.LAST_UPDATED_DATE, stg.LAST_UPDATED_DATE)      AS LAST_UPDATED_DATE
FROM DMT_OWNER.DMT_GL_INTERFACE_STG_TBL stg
LEFT JOIN (
    SELECT t.*,
           ROW_NUMBER() OVER (PARTITION BY t.STG_SEQUENCE_ID
                              ORDER BY t.TFM_SEQUENCE_ID DESC) AS rn
    FROM   DMT_OWNER.DMT_GL_INTERFACE_TFM_TBL t
) tfm ON tfm.STG_SEQUENCE_ID = stg.STG_SEQUENCE_ID AND tfm.rn = 1
LEFT JOIN DMT_OWNER.DMT_PIPELINE_RUN_TBL cm
    ON cm.RUN_ID = tfm.RUN_ID;
