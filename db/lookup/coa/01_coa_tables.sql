-- =============================================================================
-- 01_coa_tables.sql
-- Run as DMT_LOOKUP
-- All COA mapping tables. 8 tables total.
-- =============================================================================

-- 1. DMT_COA_SET — COA structure registry
CREATE TABLE DMT_COA_SET (
    COA_SET_ID          NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    SET_CODE            VARCHAR2(50)   NOT NULL,
    SET_TYPE            VARCHAR2(10)   NOT NULL,
    DESCRIPTION         VARCHAR2(500),
    SEGMENT_DELIMITER   VARCHAR2(5)    NOT NULL,
    NUM_SEGMENTS        NUMBER(2)      NOT NULL,
    LEDGER_NAME         VARCHAR2(150),
    CHART_OF_ACCOUNTS_ID NUMBER,
    ACTIVE_FLAG         VARCHAR2(1)    DEFAULT 'Y' NOT NULL,
    CREATED_DATE        DATE           DEFAULT SYSDATE,
    CONSTRAINT DMT_COA_SET_PK PRIMARY KEY (COA_SET_ID),
    CONSTRAINT DMT_COA_SET_UK UNIQUE (SET_CODE),
    CONSTRAINT DMT_COA_SET_TYPE_CK CHECK (SET_TYPE IN ('SOURCE','TARGET'))
);

-- 2. DMT_COA_SEGMENT_DEF — segment positions and names
CREATE TABLE DMT_COA_SEGMENT_DEF (
    SEGMENT_DEF_ID      NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    COA_SET_ID          NUMBER         NOT NULL,
    SEGMENT_NUM         NUMBER(2)      NOT NULL,
    SEGMENT_NAME        VARCHAR2(100)  NOT NULL,
    SEGMENT_QUALIFIER   VARCHAR2(50),
    VALUE_SET_NAME      VARCHAR2(100),
    DESCRIPTION         VARCHAR2(500),
    CONSTRAINT DMT_COA_SEGDEF_PK PRIMARY KEY (SEGMENT_DEF_ID),
    CONSTRAINT DMT_COA_SEGDEF_UK UNIQUE (COA_SET_ID, SEGMENT_NUM),
    CONSTRAINT DMT_COA_SEGDEF_FK FOREIGN KEY (COA_SET_ID)
        REFERENCES DMT_COA_SET (COA_SET_ID)
);

-- 3. DMT_COA_MAP_CONFIG — links source to target COA set
CREATE TABLE DMT_COA_MAP_CONFIG (
    MAP_CONFIG_ID       NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CONFIG_CODE         VARCHAR2(50)   NOT NULL,
    SOURCE_COA_SET_ID   NUMBER         NOT NULL,
    TARGET_COA_SET_ID   NUMBER         NOT NULL,
    DESCRIPTION         VARCHAR2(500),
    ACTIVE_FLAG         VARCHAR2(1)    DEFAULT 'Y' NOT NULL,
    CREATED_DATE        DATE           DEFAULT SYSDATE,
    CONSTRAINT DMT_COA_MC_PK PRIMARY KEY (MAP_CONFIG_ID),
    CONSTRAINT DMT_COA_MC_UK UNIQUE (CONFIG_CODE),
    CONSTRAINT DMT_COA_MC_SRC_FK FOREIGN KEY (SOURCE_COA_SET_ID)
        REFERENCES DMT_COA_SET (COA_SET_ID),
    CONSTRAINT DMT_COA_MC_TGT_FK FOREIGN KEY (TARGET_COA_SET_ID)
        REFERENCES DMT_COA_SET (COA_SET_ID)
);

-- 4. DMT_COA_SOURCE_COMBOS — source system code combinations
CREATE TABLE DMT_COA_SOURCE_COMBOS (
    SOURCE_COMBO_ID     NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    COA_SET_ID          NUMBER         NOT NULL,
    CONCAT_SEGMENTS     VARCHAR2(500)  NOT NULL,
    SEGMENT1            VARCHAR2(30),
    SEGMENT2            VARCHAR2(30),
    SEGMENT3            VARCHAR2(30),
    SEGMENT4            VARCHAR2(30),
    SEGMENT5            VARCHAR2(30),
    SEGMENT6            VARCHAR2(30),
    SEGMENT7            VARCHAR2(30),
    SEGMENT8            VARCHAR2(30),
    SEGMENT9            VARCHAR2(30),
    SEGMENT10           VARCHAR2(30),
    ENABLED_FLAG        VARCHAR2(1)    DEFAULT 'Y',
    LAST_REFRESH_DATE   DATE,
    CONSTRAINT DMT_COA_SC_PK PRIMARY KEY (SOURCE_COMBO_ID),
    CONSTRAINT DMT_COA_SC_UK UNIQUE (COA_SET_ID, CONCAT_SEGMENTS),
    CONSTRAINT DMT_COA_SC_FK FOREIGN KEY (COA_SET_ID)
        REFERENCES DMT_COA_SET (COA_SET_ID)
);

-- 5. DMT_COA_TARGET_COMBOS — valid Fusion code combinations
CREATE TABLE DMT_COA_TARGET_COMBOS (
    TARGET_COMBO_ID     NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    COA_SET_ID          NUMBER         NOT NULL,
    CONCAT_SEGMENTS     VARCHAR2(500)  NOT NULL,
    SEGMENT1            VARCHAR2(30),
    SEGMENT2            VARCHAR2(30),
    SEGMENT3            VARCHAR2(30),
    SEGMENT4            VARCHAR2(30),
    SEGMENT5            VARCHAR2(30),
    SEGMENT6            VARCHAR2(30),
    SEGMENT7            VARCHAR2(30),
    SEGMENT8            VARCHAR2(30),
    SEGMENT9            VARCHAR2(30),
    SEGMENT10           VARCHAR2(30),
    ENABLED_FLAG        VARCHAR2(1)    DEFAULT 'Y',
    LAST_REFRESH_DATE   DATE,
    CONSTRAINT DMT_COA_TC_PK PRIMARY KEY (TARGET_COMBO_ID),
    CONSTRAINT DMT_COA_TC_UK UNIQUE (COA_SET_ID, CONCAT_SEGMENTS),
    CONSTRAINT DMT_COA_TC_FK FOREIGN KEY (COA_SET_ID)
        REFERENCES DMT_COA_SET (COA_SET_ID)
);

-- 6. DMT_COA_MAPPING — the runtime crosswalk (primary table)
CREATE TABLE DMT_COA_MAPPING (
    COA_MAPPING_ID      NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    MAP_CONFIG_ID       NUMBER         NOT NULL,
    SOURCE_CONCAT       VARCHAR2(500)  NOT NULL,
    TARGET_CONCAT       VARCHAR2(500)  NOT NULL,
    TARGET_SEGMENT1     VARCHAR2(30),
    TARGET_SEGMENT2     VARCHAR2(30),
    TARGET_SEGMENT3     VARCHAR2(30),
    TARGET_SEGMENT4     VARCHAR2(30),
    TARGET_SEGMENT5     VARCHAR2(30),
    TARGET_SEGMENT6     VARCHAR2(30),
    TARGET_SEGMENT7     VARCHAR2(30),
    TARGET_SEGMENT8     VARCHAR2(30),
    TARGET_SEGMENT9     VARCHAR2(30),
    TARGET_SEGMENT10    VARCHAR2(30),
    SOURCE_TYPE         VARCHAR2(20)   DEFAULT 'MANUAL' NOT NULL,
    ACTIVE_FLAG         VARCHAR2(1)    DEFAULT 'Y' NOT NULL,
    SUGGESTED_FLAG      VARCHAR2(1)    DEFAULT 'N' NOT NULL,
    NOTES               VARCHAR2(500),
    CREATED_BY          VARCHAR2(100),
    CREATED_DATE        DATE           DEFAULT SYSDATE,
    LAST_UPDATE_BY      VARCHAR2(100),
    LAST_UPDATE_DATE    DATE,
    CONSTRAINT DMT_COA_MAP_PK PRIMARY KEY (COA_MAPPING_ID),
    CONSTRAINT DMT_COA_MAP_UK UNIQUE (MAP_CONFIG_ID, SOURCE_CONCAT),
    CONSTRAINT DMT_COA_MAP_FK FOREIGN KEY (MAP_CONFIG_ID)
        REFERENCES DMT_COA_MAP_CONFIG (MAP_CONFIG_ID),
    CONSTRAINT DMT_COA_MAP_SRC_CK CHECK (SOURCE_TYPE IN ('MANUAL','GENERATED','IMPORTED'))
);

CREATE INDEX DMT_COA_MAP_SRC_N1 ON DMT_COA_MAPPING (MAP_CONFIG_ID, SOURCE_CONCAT);

-- 7. DMT_COA_SEGMENT_MAP — segment-level rules (authoring tool)
CREATE TABLE DMT_COA_SEGMENT_MAP (
    SEGMENT_MAP_ID      NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    MAP_CONFIG_ID       NUMBER         NOT NULL,
    SOURCE_SEGMENT_NUM  NUMBER(2),
    TARGET_SEGMENT_NUM  NUMBER(2)      NOT NULL,
    MAPPING_RULE        VARCHAR2(30)   DEFAULT 'VALUE_MAP' NOT NULL,
    DEFAULT_VALUE       VARCHAR2(100),
    PAD_LENGTH          NUMBER(3),
    PAD_CHAR            VARCHAR2(1)    DEFAULT '0',
    ACTIVE_FLAG         VARCHAR2(1)    DEFAULT 'Y' NOT NULL,
    NOTES               VARCHAR2(500),
    CONSTRAINT DMT_COA_SM_PK PRIMARY KEY (SEGMENT_MAP_ID),
    CONSTRAINT DMT_COA_SM_FK FOREIGN KEY (MAP_CONFIG_ID)
        REFERENCES DMT_COA_MAP_CONFIG (MAP_CONFIG_ID),
    CONSTRAINT DMT_COA_SM_RULE_CK CHECK (
        MAPPING_RULE IN ('VALUE_MAP','PASS_THROUGH','CONSTANT','PAD_LEFT','PAD_RIGHT')
    )
);

-- 8. DMT_COA_SEGMENT_VALUES — segment value crosswalk (authoring tool)
CREATE TABLE DMT_COA_SEGMENT_VALUES (
    SEGMENT_VALUE_ID    NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    SEGMENT_MAP_ID      NUMBER         NOT NULL,
    SOURCE_VALUE        VARCHAR2(100)  NOT NULL,
    TARGET_VALUE        VARCHAR2(100)  NOT NULL,
    ACTIVE_FLAG         VARCHAR2(1)    DEFAULT 'Y' NOT NULL,
    SUGGESTED_FLAG      VARCHAR2(1)    DEFAULT 'N' NOT NULL,
    MATCH_SCORE         NUMBER,
    NOTES               VARCHAR2(500),
    CREATED_BY          VARCHAR2(100),
    CREATED_DATE        DATE           DEFAULT SYSDATE,
    LAST_UPDATE_BY      VARCHAR2(100),
    LAST_UPDATE_DATE    DATE,
    CONSTRAINT DMT_COA_SV_PK PRIMARY KEY (SEGMENT_VALUE_ID),
    CONSTRAINT DMT_COA_SV_UK UNIQUE (SEGMENT_MAP_ID, SOURCE_VALUE),
    CONSTRAINT DMT_COA_SV_FK FOREIGN KEY (SEGMENT_MAP_ID)
        REFERENCES DMT_COA_SEGMENT_MAP (SEGMENT_MAP_ID)
);
